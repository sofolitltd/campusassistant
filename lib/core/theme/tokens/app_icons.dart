/// Icon size scale for the Campus Assistant design system.
///
/// Usage: `Icon(LucideIcons.search, size: IconSize.md)`
abstract final class IconSize {
  static const double sm = 16; // inline with small text, trailing arrows
  static const double md = 20; // inline with body text
  static const double lg = 24; // nav bar, list leading icons (default)
  static const double xl = 32; // section headers, feature icons
  static const double xxl = 40; // avatar placeholder icons
}
